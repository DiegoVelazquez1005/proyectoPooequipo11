    private void actualizar1bActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizar1bActionPerformed
        usersArch.clear();
        tareasDisp.clear();
        
        usersArch.addAll(ar.archivos(true));
        tareasDisp.addAll(ar.archivos(false));
        
        idtrabajadorcombo.removeAllItems();
        tareasCombo.removeAllItems();
        asidtarea.removeAllItems();
        asigTrabajadortxt.removeAllItems();
        idtraAsigcombo1.removeAllItems();
        
        for(int i = 0; i < tareasDisp.size(); i++) {
            tareasCombo.addItem(tareasDisp.get(i).toString());
        }
        for(int i = 0; i < tareasDisp.size(); i++) {
            asidtarea.addItem(tareasDisp.get(i).toString());
        }
        for(int i = 0; i < usersArch.size(); i++) {
            idtraAsigcombo1.addItem(usersArch.get(i).toString());
        }
        for(int i = 0; i < usersArch.size(); i++) {
            idtrabajadorcombo.addItem(usersArch.get(i).toString());
        }
        for(int i = 0; i < usersArch.size(); i++) {

            asigTrabajadortxt.addItem(usersArch.get(i).toString());

        }
    }//GEN-LAST:event_actualizar1bActionPerformed

