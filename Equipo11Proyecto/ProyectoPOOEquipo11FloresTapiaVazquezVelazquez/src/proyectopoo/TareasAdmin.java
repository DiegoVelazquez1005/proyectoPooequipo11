    private void cargarTbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargarTbActionPerformed

        String idrelatra = idtraAsigcombo1.getSelectedItem().toString();
        ArrayList rela = new ArrayList();
        rela.addAll(archivos.obtenerRelacion(idrelatra));
        if(!rela.isEmpty()){
            mostrarRelatxt.setText("Estado: "+ rela.get(0)+ "   |    Nombre Tarea: " + rela.get(1));
            deteleb.setVisible(true);
        }else{
            mostrarRelatxt.setText("No hay tarea asignada");
        }
        
        
    }//GEN-LAST:event_cargarTbActionPerformed

